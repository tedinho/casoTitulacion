    {
        $fecha = new FechaConfiguracione();
        $fecha->fecha = $request['fecha'];
        $fecha->user_id = $request['user_id'];
        $fecha->save();

        return response()->json([
            "success" => true,
            "message" => "Fecha guardada de forma correcta",
            "fecha" => $fecha
        ]);
    }

